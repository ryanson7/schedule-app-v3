"use client";
import { useState, useEffect } from "react";
import { BaseSchedule, Location, ScheduleType } from "../core/types";
import { Input } from "./Input";
import { Select } from "./Select";
import { TextArea } from "./TextArea";

interface ScheduleModalProps {
  open: boolean;
  onClose: () => void;
  scheduleType: ScheduleType;
  initialData?: {
    date: string;
    locationId: number;
    scheduleData?: BaseSchedule;
  };
  locations: Location[];
  userRole: 'admin' | 'manager' | 'user';
  onSave: (data: BaseSchedule, action: 'temp' | 'request' | 'approve') => Promise<{ success: boolean; message: string }>;
}

export default function ScheduleModal({
  open,
  onClose,
  scheduleType,
  initialData,
  locations,
  userRole,
  onSave
}: ScheduleModalProps) {
  const [formData, setFormData] = useState<BaseSchedule>({
    shoot_date: '',
    start_time: '',
    end_time: '',
    professor_name: '',
    course_name: '',
    course_code: '',
    shooting_type: scheduleType === 'academy' ? 'Ï¥¨ÏòÅ' : 'PPT',
    notes: '',
    sub_location_id: 0,
    schedule_type: scheduleType,
    approval_status: 'temp',
    team_id: 1,
    is_active: true
  });

  const [errors, setErrors] = useState<Record<string, string>>({});
  const [loading, setLoading] = useState(false);

  // Ï¥àÍ∏∞ Îç∞Ïù¥ÌÑ∞ ÏÑ§Ï†ï
  useEffect(() => {
    if (open && initialData) {
      setFormData(prev => ({
        ...prev,
        shoot_date: initialData.date || '',
        sub_location_id: initialData.locationId || 0,
        schedule_type: scheduleType,
        ...(initialData.scheduleData || {})
      }));
      setErrors({});
    }
  }, [open, initialData, scheduleType]);

  // Ìèº Î¶¨ÏÖã
  useEffect(() => {
    if (!open) {
      setFormData({
        shoot_date: '',
        start_time: '',
        end_time: '',
        professor_name: '',
        course_name: '',
        course_code: '',
        shooting_type: scheduleType === 'academy' ? 'Ï¥¨ÏòÅ' : 'PPT',
        notes: '',
        sub_location_id: 0,
        schedule_type: scheduleType,
        approval_status: 'temp',
        team_id: 1,
        is_active: true
      });
      setErrors({});
    }
  }, [open, scheduleType]);

  // ÌïÑÎìú ÏóÖÎç∞Ïù¥Ìä∏
  const updateField = (field: keyof BaseSchedule, value: any) => {
    setFormData(prev => ({
      ...prev,
      [field]: field === 'sub_location_id' ? Number(value) : String(value)
    }));

    // ÏóêÎü¨ ÌÅ¥Î¶¨Ïñ¥
    if (errors[field]) {
      setErrors(prev => {
        const newErrors = { ...prev };
        delete newErrors[field];
        return newErrors;
      });
    }
  };

  // Ìèº Í≤ÄÏ¶ù
  const validateForm = (): boolean => {
    const newErrors: Record<string, string> = {};

    if (!formData.shoot_date) {
      newErrors.shoot_date = 'Ï¥¨ÏòÅ ÎÇ†ÏßúÎ•º ÏÑ†ÌÉùÌïòÏÑ∏Ïöî';
    }

    if (!formData.start_time) {
      newErrors.start_time = 'ÏãúÏûë ÏãúÍ∞ÑÏùÑ ÏÑ†ÌÉùÌïòÏÑ∏Ïöî';
    }

    if (!formData.end_time) {
      newErrors.end_time = 'Ï¢ÖÎ£å ÏãúÍ∞ÑÏùÑ ÏÑ†ÌÉùÌïòÏÑ∏Ïöî';
    }

    if (formData.start_time && formData.end_time && formData.start_time >= formData.end_time) {
      newErrors.end_time = 'Ï¢ÖÎ£å ÏãúÍ∞ÑÏùÄ ÏãúÏûë ÏãúÍ∞ÑÎ≥¥Îã§ Îä¶Ïñ¥Ïïº Ìï©ÎãàÎã§';
    }

    if (!formData.professor_name.trim()) {
      newErrors.professor_name = 'ÍµêÏàòÎ™ÖÏùÑ ÏûÖÎ†•ÌïòÏÑ∏Ïöî';
    }

    if (!formData.shooting_type) {
      newErrors.shooting_type = 'Ï¥¨ÏòÅÌòïÏãùÏùÑ ÏÑ†ÌÉùÌïòÏÑ∏Ïöî';
    }

    if (!formData.sub_location_id || formData.sub_location_id === 0) {
      newErrors.sub_location_id = `${scheduleType === 'academy' ? 'Í∞ïÏùòÏã§' : 'Ïä§ÌäúÎîîÏò§'}ÏùÑ ÏÑ†ÌÉùÌïòÏÑ∏Ïöî`;
    }

    setErrors(newErrors);
    return Object.keys(newErrors).length === 0;
  };

  // Ï†ÄÏû• Ï≤òÎ¶¨
  const handleSave = async (action: 'temp' | 'request' | 'approve') => {
    if (!validateForm()) {
      return;
    }

    setLoading(true);
    try {
      const result = await onSave(formData, action);
      
      if (result.success) {
        alert(result.message);
        onClose();
      } else {
        alert(result.message);
      }
    } catch (error) {
      console.error('Ï†ÄÏû• Ïò§Î•ò:', error);
      alert('Ï†ÄÏû• Ï§ë Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§.');
    } finally {
      setLoading(false);
    }
  };

  // ÏãúÍ∞Ñ ÏòµÏÖò ÏÉùÏÑ±
  const generateTimeOptions = () => {
    const times = [];
    for (let hour = 9; hour < 22; hour++) {
      for (let min = 0; min < 60; min += 30) {
        const h = hour.toString().padStart(2, "0");
        const m = min.toString().padStart(2, "0");
        times.push({ value: `${h}:${m}`, label: `${h}:${m}` });
      }
    }
    return times;
  };

  const timeOptions = generateTimeOptions();

  // Ï¥¨ÏòÅÌòïÏãù ÏòµÏÖò
  const shootingTypeOptions = scheduleType === 'academy' 
    ? [
        { value: 'Ï¥¨ÏòÅ', label: 'Ï¥¨ÏòÅ' },
        { value: 'ÎùºÏù¥Î∏å', label: 'ÎùºÏù¥Î∏å' },
        { value: 'ÎÖπÌôî', label: 'ÎÖπÌôî' },
        { value: 'ÌôîÏÉÅÍ∞ïÏùò', label: 'ÌôîÏÉÅÍ∞ïÏùò' }
      ]
    : [
        { value: 'PPT', label: 'PPT' },
        { value: 'ÏùºÎ∞òÏπ†Ìåê', label: 'ÏùºÎ∞òÏπ†Ìåê' },
        { value: 'Ï†ÑÏûêÏπ†Ìåê', label: 'Ï†ÑÏûêÏπ†Ìåê' },
        { value: 'ÌÅ¨Î°úÎßàÌÇ§', label: 'ÌÅ¨Î°úÎßàÌÇ§' },
        { value: 'PCÏôÄÏΩ§', label: 'PCÏôÄÏΩ§' },
        { value: 'PC', label: 'PC' }
      ];

  if (!open) return null;

  const modalTitle = scheduleType === 'academy' ? 'üè´ ÌïôÏõê' : 'üé¨ Ïä§ÌäúÎîîÏò§';
  const locationLabel = scheduleType === 'academy' ? 'Í∞ïÏùòÏã§' : 'Ïä§ÌäúÎîîÏò§';

  return (
    <div style={{
      position: 'fixed',
      top: 0,
      left: 0,
      right: 0,
      bottom: 0,
      backgroundColor: 'rgba(0, 0, 0, 0.5)',
      display: 'flex',
      alignItems: 'center',
      justifyContent: 'center',
      zIndex: 1000
    }}>
      <div style={{
        backgroundColor: 'white',
        borderRadius: '12px',
        padding: '24px',
        width: '600px',
        maxHeight: '90vh',
        overflow: 'auto',
        boxShadow: '0 20px 25px -5px rgba(0, 0, 0, 0.1)'
      }}>
        <div style={{
          display: 'flex',
          justifyContent: 'space-between',
          alignItems: 'center',
          marginBottom: '24px',
          paddingBottom: '16px',
          borderBottom: '1px solid #e5e7eb'
        }}>
          <h2 style={{ 
            margin: 0, 
            fontSize: '20px', 
            fontWeight: '600',
            color: '#1f2937'
          }}>
            {modalTitle} Ïä§ÏºÄÏ§Ñ {formData.id ? 'ÏàòÏ†ï' : 'Îì±Î°ù'}
          </h2>
          
          <button
            onClick={onClose}
            style={{
              background: 'none',
              border: 'none',
              fontSize: '24px',
              cursor: 'pointer',
              color: '#6b7280',
              padding: '4px'
            }}
          >
            √ó
          </button>
        </div>

        <div style={{ display: 'flex', flexDirection: 'column', gap: '20px' }}>
          {/* Í∏∞Î≥∏ Ï†ïÎ≥¥ */}
          <div style={{ display: 'grid', gridTemplateColumns: '1fr 1fr', gap: '16px' }}>
            <Input
              label="Ï¥¨ÏòÅ ÎÇ†Ïßú"
              type="date"
              value={formData.shoot_date}
              onChange={(value) => updateField('shoot_date', value)}
              error={errors.shoot_date}
              required
            />
            
            <Select
              label={locationLabel}
              value={formData.sub_location_id}
              onChange={(value) => updateField('sub_location_id', value)}
              options={[
                { value: 0, label: `${locationLabel}ÏùÑ ÏÑ†ÌÉùÌïòÏÑ∏Ïöî` },
                ...locations.map(location => ({
                  value: location.id,
                  label: `${location.main_locations?.name} - ${location.name}`
                }))
              ]}
              error={errors.sub_location_id}
              required
            />
          </div>

          {/* ÏãúÍ∞Ñ Ï†ïÎ≥¥ */}
          <div style={{ display: 'grid', gridTemplateColumns: '1fr 1fr', gap: '16px' }}>
            <Select
              label="ÏãúÏûë ÏãúÍ∞Ñ"
              value={formData.start_time}
              onChange={(value) => updateField('start_time', value)}
              options={[
                { value: '', label: 'ÏãúÏûë ÏãúÍ∞Ñ ÏÑ†ÌÉù' },
                ...timeOptions
              ]}
              error={errors.start_time}
              required
            />
            
            <Select
              label="Ï¢ÖÎ£å ÏãúÍ∞Ñ"
              value={formData.end_time}
              onChange={(value) => updateField('end_time', value)}
              options={[
                { value: '', label: 'Ï¢ÖÎ£å ÏãúÍ∞Ñ ÏÑ†ÌÉù' },
                ...timeOptions
              ]}
              error={errors.end_time}
              required
            />
          </div>

          {/* Í∞ïÏùò Ï†ïÎ≥¥ */}
          <div style={{ display: 'grid', gridTemplateColumns: '1fr 1fr', gap: '16px' }}>
            <Input
              label="ÍµêÏàòÎ™Ö"
              value={formData.professor_name}
              onChange={(value) => updateField('professor_name', value)}
              placeholder="ÍµêÏàòÎ™ÖÏùÑ ÏûÖÎ†•ÌïòÏÑ∏Ïöî"
              error={errors.professor_name}
              required
            />
            
            <Input
              label="Í∞ïÏùòÎ™Ö"
              value={formData.course_name}
              onChange={(value) => updateField('course_name', value)}
              placeholder="Í∞ïÏùòÎ™ÖÏùÑ ÏûÖÎ†•ÌïòÏÑ∏Ïöî"
            />
          </div>

          <div style={{ display: 'grid', gridTemplateColumns: '1fr 1fr', gap: '16px' }}>
            <Input
              label="Í∞ïÏùòÏΩîÎìú"
              value={formData.course_code || ''}
              onChange={(value) => updateField('course_code', value)}
              placeholder="Í∞ïÏùòÏΩîÎìúÎ•º ÏûÖÎ†•ÌïòÏÑ∏Ïöî"
            />
            
            <Select
              label="Ï¥¨ÏòÅÌòïÏãù"
              value={formData.shooting_type}
              onChange={(value) => updateField('shooting_type', value)}
              options={shootingTypeOptions}
              error={errors.shooting_type}
              required
            />
          </div>

          {/* ÎπÑÍ≥† */}
          <TextArea
            label="ÎπÑÍ≥†"
            value={formData.notes || ''}
            onChange={(value) => updateField('notes', value)}
            placeholder="Ï∂îÍ∞Ä Ï†ïÎ≥¥ÎÇò ÌäπÏù¥ÏÇ¨Ìï≠ÏùÑ ÏûÖÎ†•ÌïòÏÑ∏Ïöî"
            rows={3}
          />

          {/* Î≤ÑÌäº ÏòÅÏó≠ */}
          <div style={{
            display: 'flex',
            justifyContent: 'flex-end',
            gap: '12px',
            marginTop: '24px',
            paddingTop: '16px',
            borderTop: '1px solid #e5e7eb'
          }}>
            <button
              onClick={onClose}
              disabled={loading}
              style={{
                padding: '10px 20px',
                border: '1px solid #d1d5db',
                borderRadius: '6px',
                backgroundColor: 'white',
                color: '#374151',
                cursor: loading ? 'not-allowed' : 'pointer',
                fontSize: '14px',
                fontWeight: '500'
              }}
            >
              Ï∑®ÏÜå
            </button>
            
            <button
              onClick={() => handleSave('temp')}
              disabled={loading}
              style={{
                padding: '10px 20px',
                border: '1px solid #6b7280',
                borderRadius: '6px',
                backgroundColor: '#6b7280',
                color: 'white',
                cursor: loading ? 'not-allowed' : 'pointer',
                fontSize: '14px',
                fontWeight: '500'
              }}
            >
              ÏûÑÏãúÏ†ÄÏû•
            </button>
            
            {userRole === 'admin' ? (
              <button
                onClick={() => handleSave('approve')}
                disabled={loading}
                style={{
                  padding: '10px 20px',
                  border: '1px solid #22c55e',
                  borderRadius: '6px',
                  backgroundColor: '#22c55e',
                  color: 'white',
                  cursor: loading ? 'not-allowed' : 'pointer',
                  fontSize: '14px',
                  fontWeight: '500'
                }}
              >
                ÏäπÏù∏ ÏôÑÎ£å
              </button>
            ) : (
              <button
                onClick={() => handleSave('request')}
                disabled={loading}
                style={{
                  padding: '10px 20px',
                  border: '1px solid #3b82f6',
                  borderRadius: '6px',
                  backgroundColor: '#3b82f6',
                  color: 'white',
                  cursor: loading ? 'not-allowed' : 'pointer',
                  fontSize: '14px',
                  fontWeight: '500'
                }}
              >
                Îì±Î°ù ÏöîÏ≤≠
              </button>
            )}
          </div>
        </div>
      </div>
    </div>
  );
}
